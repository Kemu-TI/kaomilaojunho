    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed

        JTexFieldMatricula.setEnabled(false);
        JTexFieldNome.setEnabled(true);
        JTexFieldUsuario.setEnabled(true);
        JTexFieldSenha.setEnabled(true);
        JComboBoxTipo.setEnabled(true);
        JTexFieldPesquisar.setEnabled(true);

        UsuarioDao userdao = new UsuarioDao();
        Usuario a = new Usuario();

        String busca = JTexFieldPesquisar.getText();
        String nome = JTexFieldNome.getText();
        String usuario = JTexFieldUsuario.getText();
        String senha = JTexFieldSenha.getText();
        String tipo = (String) JComboBoxTipo.getSelectedItem();

        a = userdao.Buscar(busca);

        if (a != null) {
            JTexFieldNome.setText(a.getNome());
            JTexFieldUsuario.setText(a.getUsuario());
            JTexFieldMatricula.setText(a.getMatricula().toString());
            JComboBoxTipo.setSelectedItem(a.getTipo());
            JTexFieldSenha.setText(a.getSenha());

        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuario Não Encontrado");
        }
// TODO add your handling code here:
    }//GEN-LAST:event_jButtonBuscarActionPerformed

