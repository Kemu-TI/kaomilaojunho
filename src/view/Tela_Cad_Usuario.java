    private void LimparCampos() {
        JTexFieldMatricula.setText("");
        JTexFieldNome.setText("");
        JTexFieldUsuario.setText("");
        JTexFieldSenha.setText("");
        JTexFieldMatricula.setText("");
        JTexFieldPesquisar.setText("");
    }

    private void JButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonSalvarActionPerformed

        if (JTexFieldUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o Usuário para continuar");
            JTexFieldUsuario.requestFocus();

        } else if (JTexFieldSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha a Senha para continuar");
            JTexFieldSenha.requestFocus();
        }
        mod.setNome(JTexFieldNome.getText());
        mod.setUsuario(JTexFieldUsuario.getText());
        mod.setSenha(JTexFieldSenha.getText());
        mod.setMatricula(Integer.parseInt(JTexFieldMatricula.getText()));
        mod.setTipo((String) JComboBoxTipo.getSelectedItem());

        cadastro.Salvar(mod);
        LimparCampos();

        // TODO add your handling code here:
    }//GEN-LAST:event_JButtonSalvarActionPerformed

